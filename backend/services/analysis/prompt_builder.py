"""Prompt 构建器 — 根据分析类型生成 LLM prompt"""


def build_analysis_prompt(
    stock_name: str,
    symbol: str,
    market_data: str,
    fund_flow_data: str,
    analysis_type: str = 'comprehensive',
    v8_scores: dict | None = None,
) -> str:
    """构建分析用的 prompt"""

    base_instruction = '''你是一位有20年经验的A股市场分析师，擅长用大白话解释市场行为逻辑。
你的任务是分析以下A股数据，输出普通人能看懂的深度分析。

核心原则：
1. 禁止堆砌专业术语，全部翻译成人话
2. 禁止只说"可能上涨/下跌"，要说清楚谁在买/谁在卖/为什么
3. 重点分析市场行为逻辑，而不是预测涨跌
4. 如果信号不明确，诚实说"看不清楚"，不要硬编理由
5. 输出结构清晰，用标题分段
6. 结构化评分是规则引擎给出的事实证据包；你可以做二次判读和冲突仲裁，但不能无证据覆盖规则结论
7. 如果结构化评分存在 risk_flags 或多阶段分数接近，必须降低语气强度，明确写出反证和观察点
'''

    if analysis_type == 'comprehensive':
        instruction = base_instruction + '''
请从以下五个维度综合分析这只股票：

## 一、当前状态（一句话总结）
用一句话说清楚这只股票现在处于什么状态。

## 二、主力行为分析
结合量价关系和大单资金流向，判断主力当前在做什么：
- 是吸筹、洗盘、主升还是出货？
- 判断依据是什么？(成交量、价格位置、资金流向)
- 如果规则评分与风险信号冲突，说明是"偏主升但高位分歧"、"偏吸筹但弱势未确认"这类复合状态
- 给出置信度（高/中/低）

## 三、市场情绪与位置
- 当前股价处于什么位置（低位/中位/高位）
- 市场情绪如何（冷清/温和/活跃/狂热）
- 成交量是否配合价格走势

## 四、风险与机会
- 当前最大的风险是什么？
- 当前最大的看点是什么？
- 给出操作建议（大白话）

## 五、预期差分析
检查是否存在以下市场反常现象，如果有则重点分析原因：
- 利好不涨：有利好消息但股价不涨，可能是利好已提前定价或主力借机出货
- 业绩增长反而跌：业绩好但股价跌，是否利好出尽
- 利空落地反而涨：利空出尽变利好，市场情绪反转
- 放量涨次日跌：放量拉高后出货
- 缩量上涨：主力锁仓还是买盘不足，取决于位置高低
- 高位放量滞涨：高位放量出货信号

判断以上场景是否与当前数据吻合，并解释为何存在预期差。
重要：预期差分析的结论必须与前面四部分保持一致，不能自相矛盾。

记住：不确定就说"看不清楚"，不要糊弄。
'''
    elif analysis_type == 'main_capital':
        instruction = base_instruction + '''
请重点分析主力资金行为，严格按照以下规则判断：

吸筹特征：股价低于60日均价90%、成交量低于20日均量、大单连续3日净流入、换手率1%-3%、多根下影线
洗盘特征：在近期高点下方10%-20%、量缩至20日均量60%以下、大单小幅净流出、振幅小于3%、换手率低于1.5%
主升特征：突破前高站上全部均线、量高于20日均量150%、大单持续净流入、连续上涨不破MA5
出货特征：累计涨幅超30%、高位放量滞涨、大单净流出持续、换手率超5%

请判断当前主力处于哪个阶段，输出置信度，并用一句话解释判断依据。
'''
    elif analysis_type == 'emotion':
        instruction = base_instruction + '''
请分析当前市场情绪周期阶段：

判断标准：
- 冰点期：涨停<10只，炸板率>60%，连板高度2板以下，成交量极度萎缩
- 修复期：涨停10-20只，炸板率40-60%，连板高度3-4板，量能温和放大
- 主升期：涨停20-50只，炸板率<30%，连板高度5板以上，量能充沛
- 高潮期：涨停>50只，市场全面沸腾，量能极致放大
- 分歧期：涨停数骤降，炸板率飙升，连板高标断板
- 退潮期：涨停<15只，亏钱效应扩散，量能萎缩

请判断当前处于哪个阶段，给出依据。
'''
    else:
        instruction = base_instruction

    score_text = ''
    if v8_scores:
        score_text = f'''\n\n【结构化评分与证据包】
{v8_scores}

请按以下顺序使用这份证据：
1. 先复述规则引擎的阶段、分数、置信度和核心依据；
2. 再检查 evidence 中的连续资金、均线排列、K线结构、相对指数强弱；
3. 如果 risk_flags 存在，必须把它作为反证，而不是忽略；
4. 最终给出"规则结论是否需要修正"：维持 / 降级 / 升级 / 看不清楚。
\n'''

    return f'''{instruction}

---

股票：{stock_name}（{symbol}）
{score_text}

【日K线数据（最近60个交易日）】
{market_data}

【资金流向数据】
{fund_flow_data}

请开始分析：
'''
